/**
 * Annotations in Depth
 * 
 * Annotations are used to specify the data type of a variable, parameter, function return value, and other types of values
 * 
 * Annotations help developers catch errors early in development by allowing them to specify what types of values can be 
 * assigned to a given variable or passed as an argument to a function 
 */


// String Representation
let myName: string = "Long Tang";
myName = "Rida Ayub";
console.log(myName);

// Number Representation
let favNumber: number = 7;
favNumber += 2;
console.log(favNumber);

// Boolean Representation
let isHard: boolean = false;
isHard = true;
console.log(isHard);